package ex_04_java_basics_operators;

public class Lab028_operators_unary {
    public static void main(String[] args){
        // Unary Operator
        int a = +10;
        int a1 = -110;
        System.out.println(a);
        System.out.println(a1);

        int b = -1;
        b = b+1;
        System.out.println(b);
    }
}
