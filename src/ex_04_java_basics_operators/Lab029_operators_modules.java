package ex_04_java_basics_operators;

public class Lab029_operators_modules {
    public static void main(String[] args){
        int a = 20;
        int b = 10;
        System.out.println(a%b);
        // Mod -> Remainder,
//        10 | 20 |  2 - Quoinet
//           | 20 |
//           -------
//             0 - Remainder
//           ----
        System.out.println(13%7);
    }
}
