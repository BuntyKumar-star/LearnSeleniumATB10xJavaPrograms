package ex_07_java_incremental_decremental;

public class Lab084_post_id {public static void main(String[] args) {
    int a = 10;
    System.out.println(++a);
    System.out.println(a);


    // POST incremenet  = Print first and then increase
    int a_post = 10;
    System.out.println(a_post++);
    System.out.println(a_post);
}
}
