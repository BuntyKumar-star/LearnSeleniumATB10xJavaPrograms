package ex_11_while_loop;

public class Lab116_while_loop {
    public static void main(String[] args){
        //initialization
        int i = 1;
        while(i<10){
            System.out.println("Value of i :"+i);
            i++;
        }
        //  I, C, U
        for (int j = 0; j < 10 ; j++) {
            System.out.println(j);
        }

    }

}
